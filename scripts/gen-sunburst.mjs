// Regenerate design-system/assets/sunburst.svg as a standalone, consistent
// woven arch using the canonical knot engine. CSS vars are resolved to the
// brand hex values so the file stands alone (var() doesn't work outside a page).
import { archBraid } from '../src/lib/knots.mjs';
import { writeFileSync } from 'node:fs';

const HEX = {
  '--bg': '#121110',
  '--green-300': '#90A587',
  '--gold-400': '#CDAA61',
  '--gold-500': '#B18A44',
};

const { svg, viewBox, width, height } = archBraid(800, 300);
let body = svg;
for (const [k, v] of Object.entries(HEX)) body = body.split(`var(${k})`).join(v);

const out =
  `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${viewBox}" width="${width}" height="${height}" fill="none">` +
  `<!-- Gilt & Grove rising-sun halo — generated by the knot engine (consistent two-strand braid + rays) -->` +
  body +
  `</svg>\n`;

writeFileSync(new URL('../design-system/assets/sunburst.svg', import.meta.url), out);
console.log(`wrote sunburst.svg (${out.length} bytes), viewBox ${viewBox}`);
